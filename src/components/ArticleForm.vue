<template>
  <div class="editor-page">
    <div class="row">
      <div class="col-md-10 offset-md-1 col-xs-12">
        <mcv-validation-errors v-if="errors" :validationErrors="errors" />
        <form @submit.prevent="onSubmit">
          <fieldset>
            <fieldset class="form-group">
              <input
                type="text"
                name=""
                class="form-control form-control-lg"
                placeholder="article-title"
                v-model="title"
              />
            </fieldset>
            <fieldset class="form-group">
              <input
                type="text"
                name=""
                class="form-control form-control-lg"
                placeholder="description"
                v-model="description"
              />
            </fieldset>
            <fieldset class="form-group">
              <textarea
                type="text"
                name=""
                class="form-control form-control-lg"
                placeholder="body"
                v-model="body"
              >
              </textarea>
            </fieldset>
            <fieldset class="form-group">
              <input
                type="text"
                name=""
                class="form-control form-control-lg"
                placeholder="taglist"
                v-model="tagList"
              />
            </fieldset>
            <fieldset class="form-group">
              <button
                type="submit"
                class="btn btn-lg pull-xs-right btn-primary"
                :disabled="isSubmitting"
              >
                Create article
              </button>
            </fieldset>
          </fieldset>
        </form>
      </div>
    </div>
  </div>
</template>

<script>
import McvValidationErrors from "@/components/ValidationErrors";

export default {
  name: "McvArticleForm",
  components: {
    McvValidationErrors,
  },
  props: {
    initialValues: {
      type: Object,
      required: true,
    },
    errors: {
      type: Object,
      required: false,
    },
    isSubmitting: {
      type: Boolean,
      required: true,
    },
  },
  data() {
    return {
      title: "",
      description: "",
      body: "",
      tagList: "",
    };
  },
  methods: {
    onSubmit() {
      const form = {
        title: this.title,
        description: this.description,
        body: this.body,
        tagList: this.tagList,
      };

      this.$emit("articleSubmit", form);
    },
  },
};
</script>
